void main() {
  int mark = 59;
  if (mark >= 50 && mark <= 60) {
    //คะแนน 50-59 ได้เกรด D
    print("D");
  } else if (mark >= 60 && mark <= 69) {
    //คะแนน 60-69 ได้เกรด C
    print("C");
  } else if (mark >= 70 && mark <= 69) {
    //คะแนน 70-79 ได้เกรด B
    print("B");
  } else if (mark >= 80 && mark <= 100) {
    // คะแนน 80-100 ได้เกรด A
    print("A");
  }
}
